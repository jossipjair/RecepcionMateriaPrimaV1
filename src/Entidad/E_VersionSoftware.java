public class E_VersionSoftware {
    
    private int idVersionSoftware;
    private String versionSoftware;

    public E_VersionSoftware() {
    }

    public E_VersionSoftware(int idVersionSoftware, String versionSoftware) {
        this.idVersionSoftware = idVersionSoftware;
        this.versionSoftware = versionSoftware;
    }

    public int getIdVersionSoftware() {
        return idVersionSoftware;
    }

    public void setIdVersionSoftware(int idVersionSoftware) {
        this.idVersionSoftware = idVersionSoftware;
    }

    public String getVersionSoftware() {
        return versionSoftware;
    }

    public void setVersionSoftware(String versionSoftware) {
        this.versionSoftware = versionSoftware;
    }


    

    
    
}
